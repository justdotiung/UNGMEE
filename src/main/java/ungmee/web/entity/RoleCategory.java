package ungmee.web.entity;

public class RoleCategory {
	private int id;
	private String name;
	
	
	public RoleCategory() {
	
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
